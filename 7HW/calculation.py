x = 0
y = 0

def init(a,b):
    global x
    global y
    x = a 
    y = b

def r_devision():
    return x / y

def r_addition():
    return x + y

def r_substraction():
    return x - y

def r_multiplication():
    return x * y

def c_devision():
    return x / y *100

def c_addition():
    return (x + y) *100

def c_substraction():
    return (x - y )*100

def c_multiplication():
    return x * y*100